package com.viaversion.viaversion.api.minecraft;

public class EulerAngle {
   private final float x;
   private final float y;
   private final float z;

   public EulerAngle(float var1, float var2, float var3) {
      this.x = var1;
      this.y = var2;
      this.z = var3;
   }

   public float getX() {
      return this.x;
   }

   public float getY() {
      return this.y;
   }

   public float getZ() {
      return this.z;
   }
}
